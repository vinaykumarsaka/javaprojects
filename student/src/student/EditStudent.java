    private void btnloadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnloadActionPerformed
        // TODO add your handling code here:
        String srollno=txtrollno.getText();
				if(srollno==null||srollno.trim().equals("")){
					JOptionPane.showMessageDialog(EditStudent.this,"Please enter rollno first!");
				}else{
				int rollno=Integer.parseInt(srollno);
				Students s=StudentDao.getStudentByRollno(rollno);
				txtname.setText(s.getName());
				txtemail.setText(s.getEmail());
				txtcourse.setText(s.getCourse());
				txtfee.setText(String.valueOf(s.getFee()));
				txtpaid.setText(String.valueOf(s.getPaid()));
				txtdue.setText(String.valueOf(s.getDue()));
				txtaddress.setText(s.getAddress());
				txtcity.setText(s.getCity());
				txtstate.setText(s.getState());
				txtcountry.setText(s.getCountry());
				txtcontactno.setText(s.getContactno());
				}
    }//GEN-LAST:event_btnloadActionPerformed

