    private void btnupdatestudentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdatestudentActionPerformed
        // TODO add your handling code here:
        String name=txtname.getText();
				String email=txtemail.getText();
				String course=txtcourse.getText();
				int fee=Integer.parseInt(txtfee.getText());
				int paid=Integer.parseInt(txtpaid.getText());
				int due=Integer.parseInt(txtdue.getText());
				String address=txtaddress.getText();
				String city=txtcity.getText();
				String state=txtstate.getText();
				String country=txtcountry.getText();
				String contactno=txtcontactno.getText();
				int rollno=Integer.parseInt(txtrollno.getText());
				
				Students s=new Students(rollno,name,email,course,fee,paid,due,address,city,state,country,contactno);
				int status=StudentDao.update(s);
				
				if(status>0){
					JOptionPane.showMessageDialog(EditStudent.this,"Student updated successfully!");
					AccountantSection.main(new String[]{});
					dispose();
				}else{
					JOptionPane.showMessageDialog(EditStudent.this,"Sorry, Unable to add student!");
				}
    }//GEN-LAST:event_btnupdatestudentActionPerformed

